# Python file that explains IF statements

# Set variable animal to "cat"
animal = "cat"

# If animal is "cat" then print "Meow"
if animal == "cat":
    print("Meow")

# If animal is "dog" then print "Woof"
if animal == "dog":
    print("Woof")

# If animal is "cow" then print "Moo"
if animal == "cow":
    print("Moo")

